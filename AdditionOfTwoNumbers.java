public class AdditionOfTwoNumbers {
    public static void main(String[] args) {
        int num1 = 3; // First three-digit number
        int num2 = 5; // Second three-digit number
        int num3 = 7; // Third three-digit number
        int sum = num1 + num2 + num3; // Calculate the sum
        System.out.println("The sum of " + num1 + ", " + num2 + ", and " + num3 + " is: " + sum); // Print the result
   }
}
